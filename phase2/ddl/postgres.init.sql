CREATE TABLE GENRE (
	GENRE_NAME	        varchar(20)	NOT NULL,
	GENRE_DESCRIPTION	varchar(300)	NULL,
	PRIMARY KEY (GENRE_NAME)
);

CREATE TABLE RATING (
	RATING_NO	        int	        NOT NULL,
	RATING_SCORE	    int	        NOT NULL,
	RATING_DESCRIPTION	varchar(100)	NULL,
	PRIMARY KEY (RATING_NO)
);

CREATE TABLE ACTOR (
	ACTOR_IDENTIFICATION_NO	varchar(20)	NOT NULL,
	ACTOR_NAME	            varchar(50)	NOT NULL,
	ACTOR_BDAY	            DATE	        NULL,
	ACTOR_NATIONALITY	    varchar(20)	NOT NULL,
	PRIMARY KEY (ACTOR_IDENTIFICATION_NO)
);

CREATE TABLE ACCOUNT (
	ACCOUNT_ID		    varchar(20)	NOT NULL,
	ACCOUNT_PW	        varchar(20)	NOT NULL,
	ACCOUNT_NAME	    varchar(50)	NOT NULL,
	ACCOUNT_BDAY	    DATE	        NULL,
	ACCOUNT_SEX	        varchar(10)	NULL,
	ACCOUNT_ADDRESS	    varchar(100)	NULL,
	ACCOUNT_PHONE	    varchar(20)	    NOT NULL,
	ACCOUNT_IDENTITY	varchar(10)	    NOT NULL,
	ACCOUNT_JOB	        varchar(50)	NULL,
	ACCOUNT_MEMBERSHIP	varchar(10)	NULL,
    PRIMARY KEY (ACCOUNT_ID)
);

CREATE TABLE MOVIE (
	MOVIE_REGISTER_NO	varchar(20)	NOT NULL,
	MOVIE_TITLE	        varchar(100)	NOT NULL,
	MOVIE_TYPE	        varchar(20)	NOT NULL,
	MOVIE_RUNTIME	    int	        NULL,
	MOVIE_START_YEAR    DATE            NULL,
	PRIMARY KEY (MOVIE_REGISTER_NO)
);

CREATE TABLE EPISODE (
	EPISODE_NO		    int	        NOT NULL,
	EPISODE_NAME	    varchar(100)	NOT NULL,
	MOVIE_REGISTER_NO	varchar(20)	NOT NULL,
	PRIMARY KEY (EPISODE_NO),
	FOREIGN KEY (MOVIE_REGISTER_NO) REFERENCES MOVIE(MOVIE_REGISTER_NO)
);

CREATE TABLE VERSION (
    VERSION_IDENTIFICATION_NO   int          NOT NULL,
	VERSION_COUNTRY	            varchar(20)	NOT NULL,
	VERSION_NAME	            varchar(100)	NOT NULL,
	MOVIE_REGISTER_NO	        varchar(20)	NOT NULL,
    PRIMARY KEY (VERSION_IDENTIFICATION_NO),
	FOREIGN KEY (MOVIE_REGISTER_NO) REFERENCES MOVIE(MOVIE_REGISTER_NO)
);

CREATE TABLE CATEGORY (
	GENRE_NAME	        varchar(10)	NOT NULL,
	MOVIE_REGISTER_NO	varchar(20)	NOT NULL,
    PRIMARY KEY (GENRE_NAME, MOVIE_REGISTER_NO),
    FOREIGN KEY (GENRE_NAME) REFERENCES GENRE(GENRE_NAME),
    FOREIGN KEY (MOVIE_REGISTER_NO) REFERENCES MOVIE(MOVIE_REGISTER_NO)
);

CREATE TABLE APPEARANCE (
	MOVIE_REGISTER_NO	    varchar(20)	NOT NULL,
	ACTOR_IDENTIFICATION_NO	varchar(20)	NOT NULL,
	PRIMARY KEY (MOVIE_REGISTER_NO, ACTOR_IDENTIFICATION_NO),
	FOREIGN KEY (MOVIE_REGISTER_NO) REFERENCES MOVIE(MOVIE_REGISTER_NO),
	FOREIGN KEY (ACTOR_IDENTIFICATION_NO) REFERENCES ACTOR(ACTOR_IDENTIFICATION_NO)
);

CREATE TABLE WRITE_RATE (
	RATING_NO	        int	        NOT NULL,
	ACCOUNT_ID		    varchar(20)	NOT NULL,
    MOVIE_REGISTER_NO	varchar(20)	NOT NULL,
	PRIMARY KEY (RATING_NO, ACCOUNT_ID, MOVIE_REGISTER_NO),
	FOREIGN KEY (RATING_NO) REFERENCES RATING(RATING_NO),
	FOREIGN KEY (ACCOUNT_ID) REFERENCES ACCOUNT(ACCOUNT_ID),
    FOREIGN KEY (MOVIE_REGISTER_NO) REFERENCES MOVIE(MOVIE_REGISTER_NO)
);

